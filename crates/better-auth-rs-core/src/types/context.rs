/// Strategy for storing OAuth `state` (`oauthConfig.storeStateStrategy`).
#[derive(Debug, Clone, Copy, PartialEq, Eq, Default)]
pub enum StoreStateStrategy {
    /// Store state in the database (the default when a database/secondary storage is configured).
    #[default]
    Database,
    /// Store state in an encrypted cookie (stateless).
    Cookie,
}

/// Resolved OAuth configuration (`oauthConfig`).
#[derive(Debug, Clone, Copy, PartialEq, Eq, Default)]
pub struct OAuthConfig {
    /// Skip the OAuth state-cookie check (dangerous; dev/staging only).
    pub skip_state_cookie_check: bool,
    /// How OAuth `state` is stored.
    pub store_state_strategy: StoreStateStrategy,
}

/// Stateless cookie-cache refresh policy (`sessionConfig.cookieRefreshCache`).
#[derive(Debug, Clone, Copy, PartialEq, Eq, Default)]
pub enum CookieRefreshCache {
    /// No automatic refresh (the default; `false`).
    #[default]
    Disabled,
    /// Refresh the cookie cache `update_age` before expiry, without a database read.
    Enabled {
        /// Seconds before expiry at which to refresh.
        update_age: Duration,
    },
}

/// Resolved session configuration (`sessionConfig`).
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub struct SessionConfig {
    /// How often a session is refreshed.
    pub update_age: Duration,
    /// How long a session lives.
    pub expires_in: Duration,
    /// How recently a session must have been created to count as "fresh" for sensitive operations.
    pub fresh_age: Duration,
    /// Stateless cookie-cache refresh policy.
    pub cookie_refresh_cache: CookieRefreshCache,
}

/// The signing/encryption secret source (`secret` / `secretConfig`): a single secret or a rotating
/// set. Secret material is never logged (redacting [`Debug`]).
#[derive(Clone)]
pub enum SecretSource {
    /// A single secret.
    Single(String),
    /// A versioned, rotating set of secrets.
    Rotating(SecretConfig),
}

impl core::fmt::Debug for SecretSource {
    fn fmt(&self, f: &mut core::fmt::Formatter<'_>) -> core::fmt::Result {
        match self {
            Self::Single(_) => f.write_str("Single(<redacted>)"),
            Self::Rotating(c) => f.debug_tuple("Rotating").field(c).finish(),
        }
    }
}

/// Whether to skip the request origin check (`skipOriginCheck`): for all requests, for specific
/// path prefixes, or never. Upstream's `boolean | string[]` as a closed enum.
#[derive(Debug, Clone, PartialEq, Eq, Default)]
pub enum SkipOriginCheck {
    /// Skip for ALL requests (dangerous — disables CSRF protection).
    All,
    /// Skip only for requests whose path matches one of these prefixes (e.g. SAML callbacks).
    Paths(Vec<String>),
    /// Enforce the origin check (the default).
    #[default]
    None,
}

