//! Upstream reference: types/context.ts
//!
//! The central type hub: the entity-typed [`InternalAdapter`] data API, the long-lived
//! [`AuthContext`], and the per-request [`GenericEndpointContext`]. These are mutually recursive
//! with `init-options.ts` (`BetterAuthOptions`), `plugin.ts` (`BetterAuthPlugin`) and `api/index.ts`
//! (`AuthMiddleware`), so they all land in one batch.
//!
//! ## Deviations from the TS (idiomatic + secure Rust)
//!
//! - **No `<Options>` generic.** Upstream threads `Options extends BetterAuthOptions` purely to
//!   drive compile-time plugin-id inference (`InferPluginID`, `BetterAuthPluginRegistry`); that
//!   inference has no runtime analog, so the Rust types are non-generic over a single concrete
//!   `BetterAuthOptions`.
//! - **No method generics.** TS `createUser<T>`/`updateUser<T>` returning `T & User` would break
//!   `dyn`-safety. We return the typed entity struct and carry plugin/extra columns in a
//!   [`WithExtra`] sidecar — see its docs.
//! - **`Option`, not exceptions, for "not found".** Upstream's `InternalAdapter` returns `null`
//!   (see `db/internal-adapter.ts`) and never throws `APIError`; the route layer mints those. So
//!   reads return `Option<T>` and fallible ops return [`InternalAdapterError`] (an infrastructure
//!   error wrapping the adapter / secondary-storage layers), mapped to `APIError` up in the routes.

