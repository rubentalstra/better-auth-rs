/// An infrastructure error from an [`InternalAdapter`] operation.
///
/// The `InternalAdapter` is a thin, entity-typed layer over the dynamic
/// [`DatabaseAdapter`](crate::db::adapter::DatabaseAdapter): "not found" is modeled as `Option`, so
/// this error channel carries only genuine backend failures (the underlying adapter or secondary
/// storage). Routes map it to an `APIError` (a 500-class response); the domain `APIError`s
/// (`USER_NOT_FOUND`, `FAILED_TO_CREATE_USER`, …) are minted by the routes, not here — matching the
/// upstream implementation, which imports no error types and returns `null` for absence.
#[derive(Debug, thiserror::Error)]
pub enum InternalAdapterError {
    /// An error from the underlying database adapter.
    #[error(transparent)]
    Adapter(#[from] AdapterError),
    /// An error from the configured secondary storage.
    #[error(transparent)]
    SecondaryStorage(#[from] SecondaryStorageError),
}

/// Result alias for [`InternalAdapter`] operations.
pub type InternalAdapterResult<T> = Result<T, InternalAdapterError>;

/// A typed entity plus the extra (plugin/option-added) columns that rode alongside it.
///
/// Upstream types create/update returns as `Entity & T` where `T` is the open
/// `Record<string, any>` of plugin-added fields. Rust has no runtime analog for that intersection,
/// and a method generic would break trait-object safety, so we return the typed `record` plus an
/// `extra` map of the columns outside the base schema. Callers that only need the base entity read
/// `record`; plugin code reads `extra`.
#[derive(Debug, Clone)]
pub struct WithExtra<R> {
    /// The typed base entity.
    pub record: R,
    /// Columns outside the base schema (plugin/option-added fields).
    pub extra: Map<String, Value>,
}

