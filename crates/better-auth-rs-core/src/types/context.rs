/// The long-lived application context (`AuthContext`).
///
/// Built once at startup (by the published crate's `create_auth_context`) and shared as
/// `Arc<AuthContext>` across every concurrent request. It is **deeply immutable**: it holds only
/// resolved config, `Arc<dyn …>` trait objects, and stored closures, and exposes behavior as
/// `&self` methods. Request-scoped mutable state (the session being set on the response) lives on
/// the per-request [`GenericEndpointContext`], never here — so two concurrent requests can never
/// observe or clobber each other's session.
///
/// Deviation from the TS `AuthContext`: it is non-generic (the `<Options>` plugin-id inference is
/// compile-time only), the request-scoped `newSession`/`session`/`setNewSession` move to
/// [`GenericEndpointContext`], and the `context/` AsyncLocalStorage machinery is replaced by
/// threading `&AuthContext` / `Arc<AuthContext>` explicitly.
///
/// ## Methods that land with later batches (fields are present so they slot in without a reshape)
///
/// - `is_trusted_origin` — the wildcard origin matcher is security-critical and ports with the
///   dedicated origin-matching util (with its own tests); the `trusted_origins` field is here now.
/// - `create_auth_cookie` — the `__Host-`/`__Secure-` prefixing logic ports with the cookies layer.
/// - `check_password` — needs the request body + adapter; ports with the password/request layer.
/// - `run_migrations` — injected by the published-crate init layer.
pub struct AuthContext {
    /// Application display name (`appName`).
    pub app_name: String,
    /// The resolved server root URL (`baseURL`).
    pub base_url: String,
    /// The library/app version (`version`).
    pub version: String,
    /// The full configuration (`options`). Behavior methods read resolved fields below, not this.
    pub options: BetterAuthOptions,
    /// Resolved trusted origins (`trustedOrigins`).
    pub trusted_origins: Vec<String>,
    /// Resolved trusted providers for account linking (`trustedProviders`).
    pub trusted_providers: Vec<String>,
    /// Resolved OAuth configuration (`oauthConfig`).
    pub oauth_config: OAuthConfig,
    /// Instantiated social providers (`socialProviders`).
    pub social_providers: Vec<DynOAuthProvider>,
    /// The managed cookie set (`authCookies`).
    pub auth_cookies: BetterAuthCookies,
    /// The logger (`logger`).
    pub logger: Arc<InternalLogger>,
    /// Resolved rate-limit configuration (`rateLimit`).
    pub rate_limit: ResolvedRateLimit,
    /// The active database adapter (`adapter`).
    pub adapter: Arc<dyn DatabaseAdapter>,
    /// The entity-typed data API over the adapter (`internalAdapter`).
    pub internal_adapter: Arc<dyn InternalAdapter>,
    /// The primary secret (`secret`). Never logged.
    pub secret: String,
    /// The secret source for rotation (`secretConfig`). Never logged.
    pub secret_config: SecretSource,
    /// Resolved session configuration (`sessionConfig`).
    pub session_config: SessionConfig,
    /// How ids are generated (`generateId`'s backing config).
    pub generate_id: GenerateIdConfig,
    /// Optional secondary storage (`secondaryStorage`).
    pub secondary_storage: Option<Arc<dyn SecondaryStorage>>,
    /// Password machinery (`password`).
    pub password: PasswordContext,
    /// The resolved table schema, with plugin extensions (`tables`).
    pub tables: BetterAuthDbSchema,
    /// Whether to skip the origin check (`skipOriginCheck`).
    pub skip_origin_check: SkipOriginCheck,
    /// Whether to skip the CSRF check (`skipCSRFCheck`).
    pub skip_csrf_check: bool,
    /// Telemetry sink (`publishTelemetry`), if configured.
    pub publish_telemetry: Option<TelemetryPublishFn>,
    /// Background-task handler (`advanced.backgroundTasks.handler`), if configured.
    pub background_tasks: Option<BackgroundTaskHandler>,
}

impl AuthContext {
    /// Find a loaded plugin by id (`getPlugin`). Returns `None` if no plugin has that id.
    ///
    /// The TS generic return-type narrowing has no runtime analog; this returns the trait object,
    /// which callers may downcast if they need a concrete plugin.
    #[must_use]
    pub fn get_plugin(&self, id: &str) -> Option<Arc<dyn BetterAuthPlugin>> {
        self.options.plugins.iter().find(|p| p.id() == id).cloned()
    }

    /// Whether a plugin with the given id is loaded (`hasPlugin`).
    #[must_use]
    pub fn has_plugin(&self, id: &str) -> bool {
        self.options.plugins.iter().any(|p| p.id() == id)
    }

    /// Generate an id for `model` (`generateId`). Returns `None` when the database/adapter should
    /// generate the id itself (the upstream `false`).
    ///
    /// `Uuid`/`Serial`/`Database` defer to the adapter (it knows the dialect's id mechanism);
    /// `Default` uses the crate's random id generator; `Custom` calls the user's function.
    #[must_use]
    pub fn generate_id(&self, model: &str, size: Option<usize>) -> Option<String> {
        match &self.generate_id {
            GenerateIdConfig::Default => Some(crate::utils::generate_id(size)),
            GenerateIdConfig::Database | GenerateIdConfig::Serial | GenerateIdConfig::Uuid => None,
            GenerateIdConfig::Custom(f) => f(GenerateIdInput {
                model: model.to_owned(),
                size,
            }),
        }
    }

    /// Run a future in the background if a handler is configured, otherwise await it inline
    /// (`runInBackgroundOrAwait`).
    ///
    /// Core depends on no async runtime, so it cannot spawn on its own — the configured
    /// [`BackgroundTaskHandler`] is the injection seam (e.g. Vercel `waitUntil`). Without one, the
    /// future is awaited inline so the work is never silently dropped. The fire-and-forget
    /// `runInBackground` (which needs a runtime to spawn) lands in the published crate.
    pub async fn run_in_background_or_await(&self, fut: BoxFuture<()>) {
        match &self.background_tasks {
            Some(handler) => handler(fut),
            None => fut.await,
        }
    }
}

impl core::fmt::Debug for AuthContext {
    fn fmt(&self, f: &mut core::fmt::Formatter<'_>) -> core::fmt::Result {
        f.debug_struct("AuthContext")
            .field("app_name", &self.app_name)
            .field("base_url", &self.base_url)
            .field("version", &self.version)
            .field("options", &self.options)
            .field("trusted_origins", &self.trusted_origins)
            .field("trusted_providers", &self.trusted_providers)
            .field("oauth_config", &self.oauth_config)
            .field(
                "social_providers",
                &format_args!("[{} provider(s)]", self.social_providers.len()),
            )
            .field("auth_cookies", &self.auth_cookies)
            .field("logger", &"<logger>")
            .field("rate_limit", &self.rate_limit)
            .field(
                "adapter",
                &format_args!("<dyn DatabaseAdapter: {}>", self.adapter.id()),
            )
            .field("internal_adapter", &"<dyn InternalAdapter>")
            .field("secret", &"<redacted>")
            .field("secret_config", &self.secret_config)
            .field("session_config", &self.session_config)
            .field("generate_id", &self.generate_id)
            .field(
                "secondary_storage",
                &self
                    .secondary_storage
                    .as_ref()
                    .map(|_| "<dyn SecondaryStorage>"),
            )
            .field("password", &self.password)
            .field("tables", &format_args!("[{} table(s)]", self.tables.len()))
            .field("skip_origin_check", &self.skip_origin_check)
            .field("skip_csrf_check", &self.skip_csrf_check)
            .field(
                "publish_telemetry",
                &self.publish_telemetry.as_ref().map(|_| "<fn>"),
            )
            .field(
                "background_tasks",
                &self.background_tasks.as_ref().map(|_| "<fn>"),
            )
            .finish()
    }
}

