/// The entity-typed data API layered over the dynamic
/// [`DatabaseAdapter`](crate::db::adapter::DatabaseAdapter) (`InternalAdapter` in `context.ts`).
///
/// Held by [`AuthContext`](crate::types::context::AuthContext) as `Arc<dyn InternalAdapter>`. This
/// is the **contract**; the concrete implementation (`create_internal_adapter`, which wires hooks,
/// transforms, id generation and secondary storage over the adapter) is a separate, later batch.
/// Defining the trait now lets the routes layer and tests code against — and mock — the interface.
#[async_trait::async_trait]
pub trait InternalAdapter: Send + Sync {
    // ── users ────────────────────────────────────────────────────────────────

    /// Create a user and a linked account in one operation (`createOAuthUser`).
    async fn create_oauth_user(
        &self,
        user: NewUser,
        account: NewOAuthAccount,
    ) -> InternalAdapterResult<OAuthUserCreated>;

    /// Create a user (`createUser`). Plugin/extra columns ride back in [`WithExtra::extra`].
    async fn create_user(&self, user: NewUser) -> InternalAdapterResult<WithExtra<User>>;

    /// List users with optional paging, sort, and filter (`listUsers`).
    async fn list_users(&self, options: ListUsersOptions) -> InternalAdapterResult<Vec<User>>;

    /// Count users matching the conditions (`countTotalUsers`).
    async fn count_total_users(&self, conditions: Vec<Where>) -> InternalAdapterResult<u64>;

    /// Find a user by email, optionally with their accounts (`findUserByEmail`).
    async fn find_user_by_email(
        &self,
        email: &str,
        options: FindUserByEmailOptions,
    ) -> InternalAdapterResult<Option<UserWithAccounts>>;

    /// Find a user by id (`findUserById`).
    async fn find_user_by_id(&self, user_id: &str) -> InternalAdapterResult<Option<User>>;

    /// Update a user by id (`updateUser`). Plugin/extra columns ride back in [`WithExtra::extra`].
    async fn update_user(
        &self,
        user_id: &str,
        data: UserUpdate,
    ) -> InternalAdapterResult<WithExtra<User>>;

    /// Update a user by email (`updateUserByEmail`).
    async fn update_user_by_email(
        &self,
        email: &str,
        data: UserUpdate,
    ) -> InternalAdapterResult<WithExtra<User>>;

    /// Set a user's credential password (`updatePassword`).
    async fn update_password(&self, user_id: &str, password: &str) -> InternalAdapterResult<()>;

    /// Delete a user (`deleteUser`).
    async fn delete_user(&self, user_id: &str) -> InternalAdapterResult<()>;

    /// Bump every session of a user so caches/refreshes re-read the latest user (`refreshUserSessions`).
    async fn refresh_user_sessions(&self, user: &User) -> InternalAdapterResult<()>;

    // ── accounts ─────────────────────────────────────────────────────────────

    /// Create an account (`createAccount`). Plugin/extra columns ride back in [`WithExtra::extra`].
    async fn create_account(
        &self,
        account: NewAccount,
    ) -> InternalAdapterResult<WithExtra<Account>>;

    /// Link an account to an existing user (`linkAccount`).
    async fn link_account(&self, account: NewAccount) -> InternalAdapterResult<Account>;

    /// All accounts owned by a user (`findAccounts`).
    async fn find_accounts(&self, user_id: &str) -> InternalAdapterResult<Vec<Account>>;

    /// Find an account by its provider-side account id + provider id (`findAccountByProviderId`).
    async fn find_account_by_provider_id(
        &self,
        account_id: &str,
        provider_id: &str,
    ) -> InternalAdapterResult<Option<Account>>;

    /// All accounts owned by a user, by user id (`findAccountByUserId`).
    async fn find_account_by_user_id(&self, user_id: &str) -> InternalAdapterResult<Vec<Account>>;

    /// Find a user (and their accounts) for an OAuth sign-in (`findOAuthUser`).
    async fn find_oauth_user(
        &self,
        email: &str,
        account_id: &str,
        provider_id: &str,
    ) -> InternalAdapterResult<Option<OAuthUserResult>>;

    /// Update an account by its primary key (`updateAccount`).
    async fn update_account(&self, id: &str, data: AccountUpdate)
    -> InternalAdapterResult<Account>;

    /// Delete an account by its primary key — the `id` column, not the `accountId` column
    /// (`deleteAccount`).
    async fn delete_account(&self, id: &str) -> InternalAdapterResult<()>;

    /// Delete every account belonging to a user (`deleteAccounts`).
    async fn delete_accounts(&self, user_id: &str) -> InternalAdapterResult<()>;

    // ── sessions ─────────────────────────────────────────────────────────────

    /// Create a session for a user (`createSession`).
    async fn create_session(
        &self,
        user_id: &str,
        options: CreateSessionOptions,
    ) -> InternalAdapterResult<Session>;

    /// List a user's sessions (`listSessions`).
    async fn list_sessions(
        &self,
        user_id: &str,
        options: ListSessionsOptions,
    ) -> InternalAdapterResult<Vec<Session>>;

    /// Resolve a session token to its session + user (`findSession`).
    async fn find_session(&self, token: &str) -> InternalAdapterResult<Option<SessionWithUser>>;

    /// Resolve many session tokens to session + user pairs (`findSessions`).
    async fn find_sessions(
        &self,
        session_tokens: &[String],
        options: ListSessionsOptions,
    ) -> InternalAdapterResult<Vec<SessionWithUser>>;

    /// Update a session by its token (`updateSession`). Returns `None` if no session matched.
    async fn update_session(
        &self,
        session_token: &str,
        session: SessionUpdate,
    ) -> InternalAdapterResult<Option<Session>>;

    /// Delete a session by its token (`deleteSession`).
    async fn delete_session(&self, token: &str) -> InternalAdapterResult<()>;

    /// Delete every session belonging to a user (`deleteUserSessions`).
    async fn delete_user_sessions(&self, user_id: &str) -> InternalAdapterResult<()>;

    /// Delete sessions by their tokens (`deleteSessions`).
    async fn delete_sessions(&self, session_tokens: &[String]) -> InternalAdapterResult<()>;

    // ── verification ───────────────────────────────────────────────────────

    /// Create a verification row (`createVerificationValue`).
    async fn create_verification_value(
        &self,
        data: NewVerification,
    ) -> InternalAdapterResult<Verification>;

    /// Find the verification row for an identifier (`findVerificationValue`). Returns the latest.
    async fn find_verification_value(
        &self,
        identifier: &str,
    ) -> InternalAdapterResult<Option<Verification>>;

    /// Update verification rows by identifier (`updateVerificationByIdentifier`).
    async fn update_verification_by_identifier(
        &self,
        identifier: &str,
        data: VerificationUpdate,
    ) -> InternalAdapterResult<Verification>;

    /// Delete verification rows by identifier (`deleteVerificationByIdentifier`).
    async fn delete_verification_by_identifier(
        &self,
        identifier: &str,
    ) -> InternalAdapterResult<()>;

    /// Atomically consume a single-use verification row by `identifier` and return it
    /// (`consumeVerificationValue`).
    ///
    /// Only the first concurrent caller receives the latest row; subsequent callers receive `None`.
    /// Consuming one row invalidates the whole identifier so stale rows cannot be replayed. Rows
    /// past their `expires_at` are treated as already invalid: the row is deleted but `None` is
    /// returned, so callers do not need to gate on `expires_at` themselves. **Callers MUST gate any
    /// state change (issue session, mint token, change password) on a non-`None` result.**
    ///
    /// Replaces the racy `find_verification_value` + `delete_verification_by_identifier` pair at
    /// single-use credential consumption sites.
    async fn consume_verification_value(
        &self,
        identifier: &str,
    ) -> InternalAdapterResult<Option<Verification>>;

    /// First-writer-wins create keyed by a deterministic primary key derived from `identifier`
    /// (`reserveVerificationValue`). Returns `true` when this caller created the row and `false`
    /// when a row for the same identifier already existed.
    ///
    /// The dual of [`consume_verification_value`](Self::consume_verification_value): reserve races
    /// to create a marker exactly once, where consume races to delete one exactly once. Use it for
    /// replay tombstones (a SAML assertion id, a JWT `jti`) where the first caller wins. The
    /// database path is atomic via the primary key; the secondary-storage-only path is best-effort
    /// under concurrency.
    async fn reserve_verification_value(
        &self,
        data: ReserveVerification,
    ) -> InternalAdapterResult<bool>;
}

