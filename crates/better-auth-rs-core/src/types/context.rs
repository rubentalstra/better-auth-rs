/// Hash a password (`password.hash`). Overridable via `options.emailAndPassword.password.hash`.
pub type PasswordHashFn =
    Arc<dyn Fn(String) -> BoxFuture<Result<String, CallbackError>> + Send + Sync>;

/// Verify a password against a hash (`password.verify`).
pub type PasswordVerifyFn =
    Arc<dyn Fn(PasswordVerifyInput) -> BoxFuture<Result<bool, CallbackError>> + Send + Sync>;

/// A boxed error from a context closure (password hashing, telemetry, …).
pub type CallbackError = Box<dyn std::error::Error + Send + Sync>;

/// Password machinery resolved onto the context (`password`).
///
/// `hash`/`verify` are stored closures because they are user-overridable (defaulting to the
/// argon2id helpers in `better-auth-rs-utils`). `checkPassword` is intentionally **not** here: it
/// needs the request body and the adapter and lands with the password/request layer.
#[derive(Clone)]
pub struct PasswordContext {
    /// The hashing function.
    pub hash: PasswordHashFn,
    /// The verification function.
    pub verify: PasswordVerifyFn,
    /// The length policy.
    pub config: PasswordConfig,
}

impl core::fmt::Debug for PasswordContext {
    fn fmt(&self, f: &mut core::fmt::Formatter<'_>) -> core::fmt::Result {
        f.debug_struct("PasswordContext")
            .field("hash", &"<fn>")
            .field("verify", &"<fn>")
            .field("config", &self.config)
            .finish()
    }
}

