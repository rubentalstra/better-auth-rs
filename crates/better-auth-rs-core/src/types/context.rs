/// Per-request session state mutated over the course of a request.
#[derive(Debug, Default)]
struct RequestSessionState {
    /// The session resolved for the current request (`session`).
    session: Option<SessionWithUser>,
    /// The session to set on the response via `Set-Cookie` (`newSession`).
    new_session: Option<SessionWithUser>,
}

/// The per-request context (`GenericEndpointContext`).
///
/// Holds an `Arc<AuthContext>` (a cheap clone of the shared app context) plus the request-scoped
/// session state. The session state lives behind a [`Mutex`] so the context can be shared as
/// `Arc<GenericEndpointContext>` into async lifecycle callbacks and database hooks while still being
/// mutated via `&self`. The mutex is safe here precisely because this object is **per request** —
/// it is never shared across requests, so there is no cross-request session bleed (the property the
/// immutable [`AuthContext`] split guarantees).
///
/// The request/response half (headers, method, path, body) is populated by the api/integration
/// layer in a later batch.
pub struct GenericEndpointContext {
    /// The shared application context.
    pub context: Arc<AuthContext>,
    session: Mutex<RequestSessionState>,
}

impl GenericEndpointContext {
    /// Create a per-request context wrapping the shared [`AuthContext`].
    #[must_use]
    pub fn new(context: Arc<AuthContext>) -> Self {
        Self {
            context,
            session: Mutex::new(RequestSessionState::default()),
        }
    }

    fn lock(&self) -> std::sync::MutexGuard<'_, RequestSessionState> {
        // Poison-tolerant: a panic in one request's handler must not wedge the per-request state.
        self.session
            .lock()
            .unwrap_or_else(std::sync::PoisonError::into_inner)
    }

    /// The session resolved for the current request, if any (`session`).
    #[must_use]
    pub fn session(&self) -> Option<SessionWithUser> {
        self.lock().session.clone()
    }

    /// Set the session resolved for the current request.
    pub fn set_session(&self, session: Option<SessionWithUser>) {
        self.lock().session = session;
    }

    /// The session queued to be written to the response (`newSession`).
    #[must_use]
    pub fn new_session(&self) -> Option<SessionWithUser> {
        self.lock().new_session.clone()
    }

    /// Queue a session to be written to the response via `Set-Cookie` (`setNewSession`).
    pub fn set_new_session(&self, session: Option<SessionWithUser>) {
        self.lock().new_session = session;
    }
}

impl core::fmt::Debug for GenericEndpointContext {
    fn fmt(&self, f: &mut core::fmt::Formatter<'_>) -> core::fmt::Result {
        f.debug_struct("GenericEndpointContext")
            .field("context", &self.context)
            .field("session", &self.lock())
            .finish()
    }
}

